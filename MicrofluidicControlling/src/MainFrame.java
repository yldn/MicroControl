    private void updatePinList(){
        pinList.clear();
        
        //OnLoad Pin List
        for (int i = 0 ; i < 32 ; i++){
            pinList.add((Integer)i);
        }
        
        Iterator iter = pumpList.iterator();
        while (iter.hasNext()){
            Pump p = (Pump)iter.next();
            this.pinList.remove((Integer)p.getPinNumber1());
            this.pinList.remove((Integer)p.getPinNumber2());
        }

    }


    
    
    
    
    
