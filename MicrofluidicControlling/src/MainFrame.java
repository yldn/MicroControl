    private void deleteTypeFromTable(java.awt.event.ActionEvent evt , JTable tab){
        DefaultTableModel model = (DefaultTableModel) tab.getModel();
        //row
        if(tab.getSelectedRow()!= -1){
            String tName =(String) model.getValueAt(tab.getSelectedRow(),0);
            int in = findTypeByName(tName);
            if(in == -1){
                Log("can't find type remove anyway"+ tName);
                 model.removeRow(tab.getSelectedRow());
                return ;
            }
//            PumpType t = typelist.get(in);
            
            typelist.remove(in);
            model.removeRow(tab.getSelectedRow());
        }
    }
    private int findTypeByName(String str){
        if(str == ""|| str == null){
            System.out.println("can't find the pump the given pump name is null");
            return -1;
        }
        for(int i=0 ; i < typelist.size();i++){
            PumpType t = typelist.get(i);
            if( t.getName().equals(str) ){
                return i;
            }
        }
        return -1;
    }

    
    
    private void deletePumpFromTable( JTable tab){
        DefaultTableModel model = (DefaultTableModel) tab.getModel();
        //row
        if(tab.getSelectedRow()!= -1){
        //pumplist
//        Log("TABLE DELETE ROW"+tab.getSelectedRow());
            String pName =(String) model.getValueAt(tab.getSelectedRow(),0);
            //remove my index
            int in = this.findPumpByName(pName);
            if(in == -1){
                System.out.println("can't find the pump ,no pump"+pName+ "exist!" );
                return ;
            }
            //pumplist
            Log("pump: "+pName+" deleted");
            Pump pump = this.pumpList.get(in);
            if(!pumpList.contains((Integer)pump.getPinNumber1())) {
                this.pinList.add((Integer)pump.getPinNumber1());
            }
            if(!pumpList.contains((Integer)pump.getPinNumber2())) {
                this.pinList.add((Integer)pump.getPinNumber2());
            }
            Collections.sort(pinList);
            
//            Log("Avaliable Pins :"+ Arrays.toString(this.pinList.toArray()));
            
            //remove from pump .remove from table
            this.pumpList.remove(in);
            showPumpList();
            model.removeRow(tab.getSelectedRow());
            
            //update  combobox
            this.mapPinListToComboBox(pumpPin1);
            this.mapPinListToComboBox(pumpPin2);
        }
    }
    
