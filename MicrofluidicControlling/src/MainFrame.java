    private long timeTolong(String str) throws ParseException {
        long out = 0;
        String[] arrOfStr = str.split(":", 0); 

        out = Long.parseLong(arrOfStr[0])*60000;
        out += Long.parseLong(arrOfStr[1])*1000;
        out += Long.parseLong(arrOfStr[2]);
        return out;
    }
    
    
